//Objeto Math 
  //Math.pow (permite potenciar un numero, el primer numero es la base y el segundo la potencia)
    Math.pow(2,10); // 1024
    Math.pow(3,2); // 9

  //Math.round (redondea un numero decimal al entero mas proximo)
    Math.round(0.49); // 0
    Math.round(0.50); // 1
    Math.round(8.49); // 8
    Math.round(9.50); // 10

  //Math.floor (redondea un numero decimal al entero de menor valor)
    Math.floor(9.99); // 9
    Math.floor(15.40); // 15
  
  //Math.ceil (redondea un numero decimal al siguiente numero entero)  
    Math.ceil(8.08); // 9
    Math.ceil(20.40); // 21

  //Math.min & Math.max (permiten conocer el valor minimo y maximo en un grupo de numeros)
    Math.min(0.1, 1, 2, 3, 4, 5, 6, 7); // 0.1
    Math.max(0.1, 1, 2, 3, 4, 5, 6, 7); // 7

